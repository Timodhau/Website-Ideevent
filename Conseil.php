            <div id="intro_conseil"><h1 id="titre_conseil">Les Clés pour un Événement Incontournable</h1><br><br>

            <img id="gif_conseil" src="Images accueil/conseil.gif">

            <br><br>

                On dit qu'une image vaut mille mots, mais quand il est question d'écrire des descriptions d'événements, les mots comptent aussi beaucoup !
                La façon dont vous présentez votre événement aide certaines personnes à décider si oui ou non elles y participeront.<br><br>
                Bien évidemment, les détails principaux (où, quand, quoi) sont essentiels, mais ce sont les spécificités intéressantes supplémentaires qui rendent un événement attrayant.<br>
                Pensez à ces détails lorsque vous écrivez la description de votre événement ou donnez ces conseils à votre équipe pour que votre prochain événement soit un « incontournable ».
                <br><br>
                Voici quelques règles qui pourront vous aider !</div><br>

            <div class="regle1"><h3 class="conseil1">Règle n°1 : attirer le regard</h3><br>
                                Le visuel des photos et d'un flyer est primordial, car c’est l'une des premières caractéristiques qui se remarque.
                                Les couleurs doivent s’avérer pertinente. Mais pertinente ne signifie pas excentrique, car si les couleurs « flashy » sont bien perçues par les jeunes générations, elles restent agressives pour les autres.
                                Il faut donc choisir la teinte des flyers en fonction du cœur de cible (la sobriété étant assimilée à l’élégance).
                                <br><br>

                            <img src="Images accueil/regle1.gif"></div><br><br>

            <div class="regle2"><h3 class="conseil2">Règle n°2 : stimuler l’attention</h3><br>
                                L’accroche est le maître-mot, puisqu’elle convainc la personne de poursuivre sa lecture.
                                Il faut mettre en valeur le message principal. L’envie du lecteur doit être aiguisée, certains mots suggestifs pouvant le motiver dans ce sens, comme « offert », « promotion », « exceptionnel »…
                                <br><br>

                            <img src="Images accueil/regle2.gif"></div><br><br>

            <div class="regle1"><h3 class="conseil1">Règle n°3 : Organiser ses informations</h3><br>
                                Toute bonne creation d'événement passe invariablement par une organisation attrayante.
                                Et pour en favoriser la lecture, il est conseillé de procéder à une organisation de la description.
                                La description permet une analyse ainsi qu’une lecture optimisées. Les clients se lassant très vite, la rapidité est essentielle.
                                <br><br>

                            <img src="Images accueil/regle3.gif"></div><br><br>


            <div class="regle2"><h3 class="conseil2">Règle n°4 : Susciter l'envie</h3><br>
                                La priorité de toute création d'événement. L’approche devra donc avant tout être pensée en termes de bénéfices : ceux du client potentiel.
                                Car sans attrait, l'événement ne présentera aucun intérêt.
                                <br><br>

                            <img src="Images accueil/regle4.gif"></div><br><br>

            <div class="regle1"><h3 class="conseil1">Règle n°5 : Demander un avis extérieur</h3><br>
                                Que ce soit pour l’orthographe ou la compréhension globale, un œil neutre et neuf est indispensable.
                                Famille, amis, collègues de bureau : chaque avis pourra faire office de vérification finale en bonne et due forme… avant validation définitive de l'événement!
                                <br><br>

                            <img src="Images accueil/regle5.gif"></div><br><br>

            <div class="regle2"><h3 class="conseil2">Règle n°6 : Les faire succomber</h3><br>
                                Pour définitivement inciter le client à céder à la tentation (en l’occurrence à participer à l'événement), la méthode est imparable : 
                                il suffit de l’appâter avec un petit cadeau, un bon de réduction, ou encore une offre spéciale… réservée exclusivement aux premiers clients. 
                                Cette notion de restriction motive d’ailleurs en général les prospects, qui veulent absolument être parmi les privilégiés. Le but ? Les inciter à se déclarer participant.
                                <br><br>

